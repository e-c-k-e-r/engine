	pod::Vector3f VertexInterp( double isolevel, pod::Vector3f p1, pod::Vector3f p2, double valp1, double valp2) {
		if ( abs( isolevel - valp1 ) < 0.00001 ) return p1;
		if ( abs( isolevel - valp2 ) < 0.00001 ) return p2;
		if ( abs( valp1 - valp2 ) < 0.00001 ) return p1;
		double mu = (isolevel - valp1) / (valp2 - valp1);
		return {
			p1.x + mu * (p2.x - p1.x),
			p1.y + mu * (p2.y - p1.y),
			p1.z + mu * (p2.z - p1.z),
		};
	}
